    private void jtbListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbListMouseClicked
        int row = jtbList.getSelectedRow();
        String id = jtbList.getValueAt(row, 0).toString();
        String descripcion = jtbList.getValueAt(row, 1).toString();
        String valor = jtbList.getValueAt(row, 2).toString();
        String tipo = jtbList.getValueAt(row, 3).toString();
        String estado = jtbList.getValueAt(row, 4).toString();
        txtId.setText(id);
        txtNombre.setText(descripcion);
        txtValue.setText(valor);
        cmbState.setSelectedItem(estado);
        cmbType.setSelectedItem(tipo);

        ctrlExamen.getParamJTable(Integer.parseInt(id));
    }//GEN-LAST:event_jtbListMouseClicked

